<div class="columnapa">
<p><b>Castanae</b> no es una empresa. Es un proyecto personal, una forma de entender la vida y de expresión personal resultado de un recorrido vital. Después de tantas experiencias vividas, desde mis estudios de Derecho hasta el ejercicio del mismo, pasando por mis vivencias emocionales que tanto marcaron mi camino y la creación de mi propia familia, sentí que había llegado el momento de darle forma a un sueño, de tratar de vivir conforme a mi guía interna, y así nació Castanae.</p>
<p>Yo entiendo el horno como un modo de expresión artística y emocional. De esas emociones nacen mis recetas del mismo modo que un pintor crea un cuadro o un escritor una novela. Por eso surge este escrito, más como una reflexión que como una memoria empresarial, porque una empresa como Castanae sólo puede entenderse desde el corazón y la pasión que una persona pone para llevarlo a cabo, ya que se da la circunstancia de que Castanane y yo formamos parte de la misma entidad.</p>
<p>Y dicho esto, muy emocionada por esta oportunidad de hablar, y orgullosa de lo que representa este trabajo os lo voy a presentar.</p>
<p>Castanae nació en diciembre de 2015, aunque hacía años que llevaba gestándose en mi interior fruto de mi amor por la cocina, transmitido de generación en generación en mi familia, y por esta tierra nuestra que nos acoge y nos ofrece unos productos maravillosos con los que trabajar.</p>
<p>Nunca dudé, a la hora de establecer el horno, en hacerlo en el antiguo horno del pan de la casa de mi abuela materna, rehabilitándolo y conservando su esencia, porque mi intención era trasladarme a vivir allí, volver a las raíces, a trabajar y a vivir al ritmo de las estaciones. Pisar el freno y de ese modo elaborar productos reposados, hechos con mimo y respeto por el medio ambiente, lo que nos lleva a establecer una nueva visión del medio rural como entorno que tiene mucho que ofrecer, no sólo como productor de materias primas.</p>
<p>Así funciona Castanae, despacio y sin prisas, utilizando productos y materias primas locales para la elaboración de nuestras recetas, lo que se traduce en unos sabores maravillosos que nuestros clientes aprecian mucho.</p>
<p>En estos momentos Castanae vende sobre todo en la provincia de Lugo, así que el trabajo comercial es constante ante la necesidad de lograr más puntos de venta. También se hacen envíos a toda España, sobre todo en Navidad gracias a los polvorones artesanos que son todo un éxito.</p>
<p>Me siento afortunada de haber podido fundar este proyecto y seguir trabajando en él para que cada año llegue a más personas, y me siento satisfecha también, porque, en primer lugar, toda la gestión de Castanae, desde la creación de las recetas, elaboración, empaquetado y distribución del producto, administración y gestión está bajo mi única responsabilidad, y en segundo lugar, porque mis clientes reconocen y disfrutan de los productos que les ofrezco, y para una repostera de "autor" como yo, no hay mayor recompensa.</p>
<p>Anjhara Gómez Rodríguez</p>
<p>Gerente y fundadora en Castanae - Repostería Artesana</p>
